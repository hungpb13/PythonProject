# Arithmetic Operators
from math import remainder

friends = 10

# Addition
friends = friends + 2
friends += 2

# Subtraction
friends = friends - 2
friends -= 2

# Multiplication
friends = friends * 2
friends *= 2

# Division
friends = friends / 2
friends /= 2

# Exponentiation
friends = friends ** 2
friends **= 2

# Modulus
remainder = friends % 3

print(f"Friends = {friends}")
print(f"Remainder = {remainder}")